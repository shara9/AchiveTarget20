
public class Overloading {

	public static void main(String[] args) {
	
				Add a=new Add();
				System.out.println(a.add(2,4));
				System.out.println(a.add(5, 5,7));
			}}
			 class Add{
				public int add(int a, int b) {
				return(a+b);
				}
				public int add(int a,int b, int c)
				{
					return(a+b+c);
				}}


